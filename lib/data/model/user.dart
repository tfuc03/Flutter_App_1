class User {
  final String name;
  final String email;
  final String phone;
  final String imageUrl;
  final int gender;
  final List<String> hobbies;

  User({
    required this.name,
    required this.email,
    required this.phone,
    required this.imageUrl,
    required this.gender,
    required this.hobbies,
  });
}
